    private void BTON_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTON_AceptarActionPerformed
Usuario User = user;
String Salida = Combo_Salida.getItemAt(Combo_Salida.getSelectedIndex());
String Destino = Combo_Destino.getItemAt(Combo_Destino.getSelectedIndex());
int Cupo = Integer.parseInt(Combo_Cupos.getItemAt(Combo_Cupos.getSelectedIndex()));
String Hora = Combo_Hora.getItemAt(Combo_Hora.getSelectedIndex());
String Info = TEXT_Info.getText();
Carro Carro = new Carro(TEXT_Vehiculo.getText(),TEXT_Placa.getText(),Cupo);
int Tarifa = Integer.parseInt(TEXT_Tarifa.getText());
String Fecha =df.format(Calendar_Date.getDate());
System.out.print(Fecha);
Trayecto Trayecto = new Trayecto(User,Salida,Destino,Cupo,Hora,Fecha,Info,Carro,Tarifa);
Trayecto.AgregarTrayecto(Trayecto);
JOptionPane.showMessageDialog(null, "Trayecto Registrado Correctamente");
this.dispose();
       
    }//GEN-LAST:event_BTON_AceptarActionPerformed

